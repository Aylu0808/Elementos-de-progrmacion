#Se ingresa la base y la altura de un triángulo y calcule su área utilizando la fórmula: Área = (base *
#altura) / 2. 

B = float(input("Ingrese la base del triangulo en cm: "))
H = float(input("Ingrese la altura del triangulo en cm: "))
A = (B * H) / 2
print("El area del triangulo es de: ", A)
